import { writeFile } from "node:fs/promises";

const sourceUrl =
  "https://data.opentransportdata.swiss/dataset/f0d5278e-4376-4cde-b02c-c5bf9a348673/resource/c59b5cd0-1024-44cb-928e-1263539321dc/download/actual-date-business-organisation.csv";
const outputPath = new URL("../packages/timetable-adapters/src/swiss-business-organisations.generated.ts", import.meta.url);

const response = await fetch(sourceUrl, {
  headers: {
    "User-Agent": "trainmap-operator-sync/0.1"
  }
});

if (!response.ok) {
  throw new Error(`Failed to fetch Swiss Business Organisations CSV: HTTP ${response.status}`);
}

const csv = new TextDecoder("utf-8").decode(await response.arrayBuffer());
const rows = parseCsv(csv, ";");
const map = new Map();

for (const row of rows) {
  const organisationNumber = row.organisationNumber?.trim();
  const name =
    cleanName(row.descriptionEn) ??
    cleanName(row.descriptionDe) ??
    cleanName(row.descriptionFr) ??
    cleanName(row.descriptionIt) ??
    cleanName(row.abbreviationEn) ??
    cleanName(row.transportCompanyBusinessRegisterName);

  if (!organisationNumber || !name || map.has(organisationNumber)) {
    continue;
  }

  map.set(organisationNumber, name);
}

const sortedEntries = [...map.entries()].sort(([left], [right]) => Number(left) - Number(right));
const generatedAt = new Date().toISOString();
const body = [
  "// Generated by scripts/update-swiss-business-organisations.mjs.",
  "// Source: opentransportdata.swiss Business Organisations v2 actual-date CSV.",
  `// Generated at: ${generatedAt}`,
  "",
  "export const swissBusinessOrganisationNames: Record<string, string> = {",
  ...sortedEntries.map(([key, value]) => `  ${asciiJson(key)}: ${asciiJson(value)},`),
  "};",
  ""
].join("\n");

await writeFile(outputPath, body, "utf8");
console.log(`Wrote ${sortedEntries.length} Swiss Business Organisation names to ${outputPath.pathname}`);

function cleanName(value) {
  const normalized = value?.replace(/\s+/g, " ").trim();
  return normalized || undefined;
}

function asciiJson(value) {
  return JSON.stringify(value).replace(/[^\x20-\x7e]/g, (character) => {
    return `\\u${character.charCodeAt(0).toString(16).padStart(4, "0")}`;
  });
}

function parseCsv(input, delimiter) {
  const records = [];
  let current = "";
  let row = [];
  let inQuotes = false;

  for (let index = 0; index < input.length; index += 1) {
    const character = input[index];
    const next = input[index + 1];

    if (character === "\"") {
      if (inQuotes && next === "\"") {
        current += "\"";
        index += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (!inQuotes && character === delimiter) {
      row.push(current);
      current = "";
      continue;
    }

    if (!inQuotes && (character === "\n" || character === "\r")) {
      if (character === "\r" && next === "\n") {
        index += 1;
      }
      row.push(current);
      current = "";
      if (row.some((cell) => cell.length > 0)) {
        records.push(row);
      }
      row = [];
      continue;
    }

    current += character;
  }

  row.push(current);
  if (row.some((cell) => cell.length > 0)) {
    records.push(row);
  }

  const [headers = [], ...dataRows] = records;
  return dataRows.map((dataRow) =>
    Object.fromEntries(headers.map((header, index) => [header.trim(), dataRow[index]?.trim() ?? ""]))
  );
}
